package Packets;

public class DataTypeId {

    public static final int
            NULL = 0x00,
            BOOLEAN = 0x01,
            BYTE = 0x02,
            SIGNED_BYTE = 0xA2,
            SHORT = 0x03,
            SIGNED_SHORT = 0xA3,
            INT = 0x04,
            SIGNED_INT = 0xA4,
            LONG = 0x05,
            SIGNED_LONG = 0xA5,
            FLOAT = 0x06,
            DOUBLE = 0x07,
            VAR_INT = 0x08,
            VAR_LONG = 0x09,
            STRING = 0x20,
            BYTES = 0x21,
            ARRAY = 0x22;
}

